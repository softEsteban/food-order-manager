package model;

public class cliente {
    
    String cl_idCliente;
    String cl_nombre;
    String cl_direccion;
    String cl_telefono;
    String cl_correo;

    public String getCl_idCliente() {
        return cl_idCliente;
    }

    public void setCl_idCliente(String cl_idCliente) {
        this.cl_idCliente = cl_idCliente;
    }

    public String getCl_nombre() {
        return cl_nombre;
    }

    public void setCl_nombre(String cl_nombre) {
        this.cl_nombre = cl_nombre;
    }

    public String getCl_direccion() {
        return cl_direccion;
    }

    public void setCl_direccion(String cl_direccion) {
        this.cl_direccion = cl_direccion;
    }

    public String getCl_telefono() {
        return cl_telefono;
    }

    public void setCl_telefono(String cl_telefono) {
        this.cl_telefono = cl_telefono;
    }

    public String getCl_correo() {
        return cl_correo;
    }

    public void setCl_correo(String cl_correo) {
        this.cl_correo = cl_correo;
    }
    
    
    
    
}
